import { existsSync, mkdirSync, writeFileSync } from 'fs';
import { join } from 'path';
import { marked } from 'marked';
import { Person, Story } from './models.js';
import { sanitizeMarkdownHtml } from './security.js';

export function exportJSON(outputDir) {
  const data = { people: Person.getAll(), stories: Story.getAllWithPeople() };
  if (!existsSync(outputDir)) mkdirSync(outputDir, { recursive: true });
  const path = join(outputDir, 'cairn-export.json');
  writeFileSync(path, JSON.stringify(data, null, 2));
  return path;
}

export function exportHTML(outputDir) {
  if (!existsSync(outputDir)) mkdirSync(outputDir, { recursive: true });

  const people = Person.getAll();
  const stories = Story.getAllWithPeople();

  function buildIndex() {
    const personCards = people.map(p => `
      <div class="card">
        <a href="person-${p.id}.html">${escapeHtml(p.name)}</a>
        ${p.birth_date ? `<span class="date">${p.birth_date}${p.death_date ? ' — ' + p.death_date : ''}</span>` : ''}
        ${p.bio ? `<p class="preview">${escapeHtml(p.bio.substring(0, 200))}</p>` : ''}
      </div>`).join('');

    const storyCards = stories.map(s => `
      <div class="card">
        <a href="story-${s.id}.html">${escapeHtml(s.title)}</a>
        ${s.story_date ? `<span class="date">${s.story_date}</span>` : ''}
        <div class="tags">${(s.tags || []).map(t => `<span class="tag">${escapeHtml(t)}</span>`).join('')}</div>
      </div>`).join('');

    return page(`Cairn — ${getName()}`, `
      <h1>${getName()}</h1>
      <p class="subtitle">A living archive of memory</p>
      <hr>
      <h2>People (${people.length})</h2>
      <div class="grid">${personCards || '<p>No people recorded yet.</p>'}</div>
      <h2>Stories (${stories.length})</h2>
      <div class="grid">${storyCards || '<p>No stories recorded yet.</p>'}</div>
      <footer>
        <p>Generated by <a href="https://github.com/cairn-project/cairn">Cairn</a> on ${new Date().toLocaleDateString()}</p>
      </footer>
    `);
  }

  function buildPerson(p) {
    const rels = Person.getRelationships(p.id);
    const personStories = Story.getByPersonId(p.id);

    const relList = rels.map(r => `
      <li><a href="person-${r.related_person_id}.html">${escapeHtml(r.related_person_name)}</a> (${r.type})</li>`).join('');

    const storyList = personStories.map(s => `
      <li><a href="story-${s.id}.html">${escapeHtml(s.title)}</a>${s.story_date ? ' <span class="date">' + s.story_date + '</span>' : ''}</li>`).join('');

    return page(escapeHtml(p.name), `
      <nav><a href="index.html">← Home</a></nav>
      <h1>${escapeHtml(p.name)}</h1>
      ${p.birth_date ? `<p class="date">${p.birth_date}${p.death_date ? ' — ' + p.death_date : ''}</p>` : ''}
      ${p.bio ? `<div class="bio">${escapeHtml(p.bio)}</div>` : ''}
      ${rels.length ? `<h2>Relationships</h2><ul>${relList}</ul>` : ''}
      ${personStories.length ? `<h2>Stories</h2><ul>${storyList}</ul>` : ''}
      <footer><a href="index.html">← Back to Home</a></footer>
    `);
  }

  function buildStory(s) {
    const peopleLinks = (s.people || []).map(p =>
      `<a href="person-${p.id}.html">${escapeHtml(p.name)}</a>`).join(', ');

    return page(escapeHtml(s.title), `
      <nav><a href="index.html">← Home</a></nav>
      <h1>${escapeHtml(s.title)}</h1>
      ${s.story_date ? `<p class="date">${s.story_date}</p>` : ''}
      ${peopleLinks ? `<p class="people">With: ${peopleLinks}</p>` : ''}
      <div class="content">${sanitizeMarkdownHtml(marked.parse(s.content))}</div>
      ${(s.tags || []).length ? `<div class="tags">${s.tags.map(t => `<span class="tag">${escapeHtml(t)}</span>`).join('')}</div>` : ''}
      <footer><a href="index.html">← Back to Home</a></footer>
    `);
  }

  writeFileSync(join(outputDir, 'index.html'), buildIndex());
  for (const p of people) writeFileSync(join(outputDir, `person-${p.id}.html`), buildPerson(p));
  for (const s of stories) writeFileSync(join(outputDir, `story-${s.id}.html`), buildStory(s));

  writeFileSync(join(outputDir, 'style.css'), globalCSS);

  return outputDir;
}

function getName() {
  try { return Person.getAll()[0]?.name || 'My Archive'; } catch { return 'My Archive'; }
}

function escapeHtml(s) {
  if (!s) return '';
  return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

